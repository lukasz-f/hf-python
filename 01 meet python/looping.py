movies = ["the Holy Grail", "The Life of Brian", "The Meaning of Life"]

#Pętla for
for movie in movies:
    print(movie)

#Pętla while
count=0
while count < len(movies):
    print(movies[count])
    count = count + 1

#Pętla for z licznikiem.
for i in range(4):
    print(i)        # Wyświetla 0, 1, 2, 3
